#hashes are organised into 'key/value pairs' meaning there is a key part and a value part, as set out below

hash_type_1 = {
	"key1" => "value1", 
	"key2" => "value2", 
	"key3" => "value3"
}

hash_type_2 = Hash.new
	hash_type2["key1"] = "value1"
	hash_type2["key2"] = "value2"
	hash_type3["key3"] = "value3"



